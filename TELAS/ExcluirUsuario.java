    private void buttonConsultaUsuarioConsultarClientes(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConsultaUsuarioConsultarClientes

        textAreaConsultaUsuario.setText("");

        if (inputConsultaUsuario.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Informe o usu치rio que deseja excluir!");

        } else {

            String sql = null;
            Connection conexao = null;
            arrayUser.clear();

            String itemSelecionado = selectConsultaUsuario.getSelectedItem().toString();

            String valorDigitado = inputConsultaUsuario.getText();

            sql = "SELECT * FROM usuario WHERE " + itemSelecionado + " LIKE '" + valorDigitado + "' ORDER BY login ASC ";
            conexao = ConnMySQL.conectar();

            try {
                Statement statement = conexao.createStatement();
                ResultSet result = statement.executeQuery(sql);

                while (result.next()) {

                    User u = new User(
                            result.getString("login"),
                            result.getString("nome"),
                            result.getString("cpf"),
                            result.getInt("admin")
                    );

                    String adm = null;

                    if (u.getAdmin() == 1) {

                        adm = "ADMINISTRADOR";
                    } else {

                        adm = "COMUM";
                    }

                    arrayUser.add(u);

                    textAreaConsultaUsuario.setText(textAreaConsultaUsuario.getText()
                            + "\nUsuario: " + u.getLogin() + "\n"
                            + "Nome: " + u.getNome() + "\n"
                            + "CPF: " + u.getCpf() + "\n"
                            + "Tipo de usu치rio " + adm + "\n\n");

                }

                conexao.close();

            } catch (SQLException throwables) {
                throwables.printStackTrace();

            } catch (Exception f) {
                System.out.println("ERRO EXCLUS츾O: " + f);
            }

        }

    }//GEN-LAST:event_buttonConsultaUsuarioConsultarClientes

    private void buttonExcluirUsuarioGerarArquivo(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExcluirUsuarioGerarArquivo

        if (inputConsultaUsuario.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Informe o usu치rio que deseja excluir!");
        } else {

            if (textAreaConsultaUsuario.getText().equals("")) {

                JOptionPane.showMessageDialog(null, "Informe o usu치rio que deseja excluir!");

            } else {

                int i = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja efetuar a exclus칚o?", "Confirma칞칚o", JOptionPane.YES_NO_OPTION);

                if (i == JOptionPane.YES_OPTION) {

                    try {

                        Connection conexao = ConnMySQL.conectar();

                        String itemSelecionado = selectConsultaUsuario.getSelectedItem().toString();

                        String valorDigitado = inputConsultaUsuario.getText();

                        String sql = "DELETE FROM usuario WHERE " + itemSelecionado + " LIKE '" + valorDigitado + "'";
                        PreparedStatement statement = conexao.prepareStatement(sql);

                        int linhas = statement.executeUpdate();

                        if (linhas > 0) {
                            textAreaConsultaUsuario.setText("");
                            inputConsultaUsuario.setText("");
                            conexao.close();
                            JOptionPane.showMessageDialog(null, "Usu치rio exclu칤do com sucesso!");
                        } else {

                            JOptionPane.showMessageDialog(null, "Usu치rio N츾O exclu칤do!");
                        }
                        conexao.close();

                    } catch (Exception f) {
                        System.out.println("ERRO EXCLUS츾O: " + f);
                    }

                } else {
                    textAreaConsultaUsuario.setText("");
                    inputConsultaUsuario.setText("");

                    JOptionPane.showMessageDialog(null, "Nenhum usu치rio exclu칤do!");
                }
            }

        }
    }//GEN-LAST:event_buttonExcluirUsuarioGerarArquivo

