    private void Entrar(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Entrar

        String sql = "SELECT * FROM usuario WHERE login like'" + inputUsuario.getText() + "'AND senha ='" + inputSenha.getText() + "'";
        Connection conexao = ConnMySQL.conectar();
        User uLogin = null;

        try {

            Statement statement = conexao.createStatement();
            ResultSet result = statement.executeQuery(sql);

            while (result.next()) {
                uLogin = new User(
                        result.getString("login"),
                        result.getString("senha"),
                        result.getString("nome"),
                        result.getString("cpf"),
                        result.getInt("id"),
                        result.getInt("admin")
                );
            }
        } catch (SQLException throwables) {
            throwables.printStackTrace();
        }

        if (uLogin != null) {

            login = uLogin.getLogin();
            adm = uLogin.getAdmin();

            PagAdm pagAdm = new PagAdm();

            pagAdm.setVisible(true);
            pagAdm.setLocationRelativeTo(null);  

            this.dispose();
            
        } else {
            JOptionPane.showMessageDialog(null, "Login e/ou senha incorreto(s).");
        }

    }//GEN-LAST:event_Entrar

