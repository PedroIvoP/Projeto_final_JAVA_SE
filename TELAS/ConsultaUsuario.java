    private void buttonGerarArquivoConsultaClienteGerarArquivo(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonGerarArquivoConsultaClienteGerarArquivo

        i++;

        try {
            FileWriter caminho = new FileWriter("C:\\JAVA\\Relatorio_de_usuarios_" + i + ".txt");
            BufferedWriter arquivo = new BufferedWriter(caminho);

            arquivo.write("\n\t\tRELATÓRIO DE USUÁRIOS\n\n");

            for (User u : arrayUser) {
                
                String adm = null;

                if(u.getAdmin() == 1){
                    
                    adm = "ADMINISTRADOR";
                }else{
                    
                    adm = "COMUM";
                }
                
                arquivo.write("\nUsuario: " + u.getLogin() + "\n"
                        + "Nome: " + u.getNome() + "\n"
                        + "CPF: " + u.getCpf() + "\n"
                        + "Tipo de usuário: " + adm + "\n\n");
            }

            arquivo.close();

            JOptionPane.showMessageDialog(null, "Arquivo gerado com sucesso!");

        } catch (FileNotFoundException ex) {

            System.out.println("Erro no arquivo(" + ex + ")");

        } catch (Exception f) {
            System.out.println("ERRO CADASTRO: " + f);
        }
    }//GEN-LAST:event_buttonGerarArquivoConsultaClienteGerarArquivo

