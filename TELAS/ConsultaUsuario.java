    private void buttonConsultaClienteConsultarClientes(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConsultaClienteConsultarClientes

        textAreaConsultaCarro.setText("");

        String sql = null;
        Connection conexao = null;
        arrayUser.clear();

        if (selectConsultaClienteGeral.isSelected()) {

            sql = "SELECT * FROM usuario ORDER BY login ASC";
            conexao = ConnMySQL.conectar();

        } else {

            String itemSelecionado = selectConsultaCliente.getSelectedItem().toString();

            String valorDigitado = inputConsultaCliente.getText();

            sql = "SELECT * FROM usuario WHERE " + itemSelecionado + " LIKE '" + valorDigitado + "' ORDER BY login ASC ";
            conexao = ConnMySQL.conectar();
        }

        try {
            Statement statement = conexao.createStatement();
            ResultSet result = statement.executeQuery(sql);

            while (result.next()) {
                
                

                User u = new User(
                        result.getString("login"),
                        result.getString("nome"),
                        result.getString("cpf"),
                        result.getInt("admin")
                );
                
                String adm = null;

                if(u.getAdmin() == 1){
                    
                    adm = "ADMINISTRADOR";
                }else{
                    
                    adm = "COMUM";
                }

                arrayUser.add(u);

                textAreaConsultaCarro.setText(textAreaConsultaCarro.getText() + 
                        "\nUsuario: " + u.getLogin() + "\n"
                        + "Nome: " + u.getNome() + "\n"
                        + "CPF: " + u.getCpf() + "\n"
                        + "Tipo de usuário: " + adm + "\n\n");
            }

            conexao.close();

        } catch (SQLException throwables) {
            throwables.printStackTrace();

        } catch (Exception f) {
            System.out.println("ERRO CADASTRO: " + f);
        }
    }//GEN-LAST:event_buttonConsultaClienteConsultarClientes

